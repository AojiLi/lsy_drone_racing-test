    def coefficients(self) -> dict[str, float]:
        """Return reward coefficients for checkpoint metadata and diagnostics."""
        return {name: float(getattr(self, name)) for name in REFERENCE_TRACKER_REWARD_DEFAULTS}

    def compute(
        self,
        prev_obs: dict[str, Any],
        obs: dict[str, Any],
        reference: ReferenceFrame,
        action_norm: np.ndarray,
        prev_action_norm: np.ndarray,
        *,
        terminated: bool,
        truncated: bool,
    ) -> tuple[float, dict[str, float]]:
        """Compute the shaped tracker reward and diagnostics for one transition."""
        pos = np.asarray(obs["pos"], dtype=np.float32)
        prev_pos = np.asarray(prev_obs["pos"], dtype=np.float32)
        vel = np.asarray(obs["vel"], dtype=np.float32)
        pos_error = float(np.linalg.norm(reference.current_point - pos))
        prev_error = float(np.linalg.norm(reference.current_point - prev_pos))
        vel_error = float(np.linalg.norm(reference.desired_velocity - vel))
        heading = safe_normalize(reference.desired_heading)
        current_heading = quat_to_rotmat(np.asarray(obs["quat"], dtype=np.float32))[:, 0]
        current_heading[2] = 0.0
        current_heading = safe_normalize(current_heading)
        heading_error = 1.0 - float(np.clip(np.dot(heading, current_heading), -1.0, 1.0))
        gate_yz_error = float(
            np.linalg.norm(reference.gate_local_position[1:3] - reference.aperture_yz)
        )
        obstacle_penalty = 0.0
        if reference.obstacle_detected > 0.5 and reference.obstacle_distance < self.obstacle_margin:
            obstacle_penalty = self.obstacle_margin - reference.obstacle_distance
        action_penalty = float(np.mean(np.square(action_norm)))
        delta_penalty = float(np.mean(np.square(action_norm - prev_action_norm)))
        gate_terms = self._gate_completion_terms(prev_obs, obs, reference, vel)
        semantic_terms = self._semantic_terms(reference, vel)
        reward = (
            -self.pos_error_coef * pos_error
            - self.vel_error_coef * vel_error
            - self.heading_coef * heading_error
            - self.gate_center_coef * gate_yz_error
            - self.obstacle_coef * obstacle_penalty
            - self.action_coef * action_penalty
            - self.action_delta_coef * delta_penalty
            + self.progress_bonus * (prev_error - pos_error)
            + self.gate_x_progress_coef * gate_terms["gate_x_progress_aligned_m"]
            + self.gate_cross_bonus * gate_terms["valid_aperture_cross_event"]
            + self.gate_recover_bonus * gate_terms["post_gate_recovery_event"]
            - self.gate_linger_penalty_coef * gate_terms["near_plane_linger_event"]
            - self.semantic_brake_speed_coef * semantic_terms["brake_hold_speed_excess"]
            - self.semantic_slow_speed_coef * semantic_terms["slow_through_speed_error"]
            - self.semantic_slow_stop_coef * semantic_terms["slow_through_stop_error"]
            - self.semantic_recover_speed_coef * semantic_terms["recover_speed_error"]
        )
        if terminated and not truncated:
            reward -= self.crash_penalty
        diagnostics = {
            "tracker/pos_error": pos_error,
            "tracker/vel_error": vel_error,
            "tracker/heading_error": heading_error,
            "tracker/gate_yz_error": gate_yz_error,
            "tracker/obstacle_penalty": obstacle_penalty,
            "tracker/action_penalty": action_penalty,
            "tracker/action_delta_penalty": delta_penalty,
            "tracker/gate_x": gate_terms["gate_x"],
            "tracker/gate_x_progress": gate_terms["gate_x_progress_m"],
            "tracker/gate_x_progress_aligned": gate_terms["gate_x_progress_aligned_m"],
            "tracker/gate_current_yz_error": gate_terms["current_aperture_yz_error"],
            "tracker/valid_aperture_cross_event": gate_terms["valid_aperture_cross_event"],
            "tracker/post_gate_recovery_event": gate_terms["post_gate_recovery_event"],
            "tracker/near_plane_linger_event": gate_terms["near_plane_linger_event"],
            "tracker/waypoint_type_id": float(reference.waypoint_type_id),
            "tracker/stop_signal": float(reference.stop_signal),
            "tracker/brake_mask": float(reference.brake_mask),
            "tracker/slow_through_mask": float(reference.slow_through_mask),
            "tracker/desired_speed_error": semantic_terms["desired_speed_error"],
            "tracker/brake_hold_speed_excess": semantic_terms["brake_hold_speed_excess"],
            "tracker/slow_through_speed_error": semantic_terms["slow_through_speed_error"],
            "tracker/slow_through_stop_error": semantic_terms["slow_through_stop_error"],
            "tracker/recover_speed_error": semantic_terms["recover_speed_error"],
        }
        return float(reward), diagnostics

    @staticmethod
    def _semantic_terms(reference: ReferenceFrame, vel: np.ndarray) -> dict[str, float]:
        """Return generic command behavior diagnostics and optional reward terms."""
        speed = float(np.linalg.norm(vel))
        desired_speed_error = abs(speed - float(reference.desired_speed))
        brake_excess = max(0.0, speed - 0.12) * float(reference.brake_mask)
        slow_speed_error = desired_speed_error * float(reference.slow_through_mask)
        slow_stop_error = max(0.0, 0.12 - speed) * float(reference.slow_through_mask)
        recover_mask = 1.0 if reference.waypoint_type == "recover_speed" else 0.0
        recover_speed_error = desired_speed_error * recover_mask
        return {
            "desired_speed_error": desired_speed_error,
            "brake_hold_speed_excess": brake_excess,
            "slow_through_speed_error": slow_speed_error,
            "slow_through_stop_error": slow_stop_error,
            "recover_speed_error": recover_speed_error,
        }

    @staticmethod
    def _gate_completion_terms(
        prev_obs: dict[str, Any], obs: dict[str, Any], reference: ReferenceFrame, vel: np.ndarray
    ) -> dict[str, float]:
        """Return gate-crossing shaping terms for gate-aperture training."""
        terms = {
            "gate_x": 0.0,
            "gate_x_progress_m": 0.0,
            "gate_x_progress_aligned_m": 0.0,
            "current_aperture_yz_error": 0.0,
            "valid_aperture_cross_event": 0.0,
            "post_gate_recovery_event": 0.0,
            "near_plane_linger_event": 0.0,
        }
        try:
            prev_gate_local = ReferenceTrackerReward._target_gate_local(
                prev_obs, reference.target_gate
            )
            gate_local = ReferenceTrackerReward._target_gate_local(obs, reference.target_gate)
        except (KeyError, IndexError, ValueError):
            return terms

        prev_x = float(prev_gate_local[0])
        gate_x = float(gate_local[0])
        gate_x_progress = max(0.0, gate_x - prev_x)
        yz_error = float(np.linalg.norm(gate_local[1:3] - reference.aperture_yz))
        alignment_scale = max(0.0, 1.0 - yz_error / 0.32)
        speed = float(np.linalg.norm(vel))

        terms["gate_x"] = gate_x
        terms["gate_x_progress_m"] = gate_x_progress
        terms["gate_x_progress_aligned_m"] = gate_x_progress * alignment_scale
        terms["current_aperture_yz_error"] = yz_error
        if prev_x < 0.0 <= gate_x and yz_error <= 0.32:
            terms["valid_aperture_cross_event"] = 1.0
        if gate_x >= 0.35 and speed <= 0.65:
            terms["post_gate_recovery_event"] = 1.0
        if -0.32 <= gate_x < -0.20 and yz_error <= 0.12 and speed <= 0.12:
            terms["near_plane_linger_event"] = 1.0
        return terms

    @staticmethod
    def _target_gate_local(obs: dict[str, Any], target_gate: int) -> np.ndarray:
        gates_pos = np.asarray(obs["gates_pos"], dtype=np.float32)
        gates_quat = np.asarray(obs["gates_quat"], dtype=np.float32)
        target_gate = int(np.clip(target_gate, 0, len(gates_pos) - 1))
        gate_pos = gates_pos[target_gate]
        gate_rot = quat_to_rotmat(gates_quat[target_gate])
        pos = np.asarray(obs["pos"], dtype=np.float32)
        return (gate_rot.T @ (pos - gate_pos)).astype(np.float32)


